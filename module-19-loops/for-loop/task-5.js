
/***
Problem-5:
    Implement a countdown timer that counts down from 81 to 65.

 */


// Solve it-----

// Use for loop
for(let count = 81; count >= 65; count--){
    console.log(count);
}

// Use while loop
let countdown = 81;
while(countdown >= 65){
    console.log(countdown);
    countdown--;
}